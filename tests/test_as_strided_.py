@pytest.mark.as_strided_
@pytest.mark.parametrize("dtype", utils.FLOAT_DTYPES)
def test_as_strided_inplace(dtype):
    """Test that as_strided_ is in-place and returns the original tensor."""
    shape = (12,)
    size = [3, 4]
    stride = [4, 1]
    storage_offset = 0

    inp = torch.randn(shape, dtype=dtype, device=flag_gems.device)
    ref_inp = utils.to_reference(inp.clone())

    # Apply as_strided_.
    ref_out = ref_inp.as_strided_(size, stride, storage_offset)

    with flag_gems.use_gems():
        res_out = inp.as_strided_(size, stride, storage_offset)

    # Verify inplace behavior.
    assert res_out is inp, "as_strided_ should return the same tensor object"
    assert ref_out is ref_inp, (
        "Reference as_strided_ should return the same tensor object"
    )

    # Verify shape, stride and storage_offset.
    assert inp.shape == torch.Size(size), (
        f"Shape mismatch: {inp.shape} vs {size}"
    )

    assert inp.stride() == tuple(stride), (
        f"Stride mismatch: {inp.stride()} vs {stride}"
    )

    assert inp.storage_offset() == storage_offset, (
        f"Storage offset mismatch: {inp.storage_offset()} vs {storage_offset}"
    )

    # Verify data is correct.
    utils.gems_assert_close(inp, ref_inp, dtype)
