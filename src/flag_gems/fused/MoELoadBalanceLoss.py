def _torch_moe_load_balance_loss(gate_logits: torch.Tensor) -> torch.Tensor:
    num_tokens, num_experts = gate_logits.shape
    softmax_probs = torch.softmax(gate_logits.float(), dim=-1)
    expert_loads = softmax_probs.sum(dim=0)
    expert_loads_normalized = expert_loads / num_tokens
    loss = num_experts * expert_loads_normalized.pow(2).sum()
    return loss.to(dtype=gate_logits.dtype)


