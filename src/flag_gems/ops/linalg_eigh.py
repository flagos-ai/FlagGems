    d = tl.load(A + pid * stride_a + 1)  # A[0, 1] or A[1, 0]
    b = tl.load(A + pid * stride_a + 3)  # A[1, 1]

    # For symmetric 2x2 matrix:
    # [a  d]
    # [d  b]
    # eigenvalues = (a+b)/2 +/- sqrt(((a-b)/2)^2 + d^2)

    trace = (a + b) / 2.0
    diff = (a - b) / 2.0
    disc = tl.sqrt(diff * diff + d * d)

    ev1 = trace + disc
    ev2 = trace - disc

    # Sort eigenvalues in ascending order
    ev_min = tl.where(ev1 < ev2, ev1, ev2)
    ev_max = tl.where(ev1 < ev2, ev2, ev1)

    # Store eigenvalues
    tl.store(eigenvalues + pid * stride_e + 0, ev_min)
    tl.store(eigenvalues + pid * stride_e + 1, ev_max)


