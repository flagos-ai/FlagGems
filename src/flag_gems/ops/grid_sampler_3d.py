    """Grid sampler 3D with trilinear or nearest interpolation."""
    logger.debug("GEMS GRID_SAMPLER_3D")

    N, C, in_d, in_h, in_w = input.shape
    out_d, out_h, out_w = grid.shape[1:4]

    output = torch.empty(
        (N, C, out_d, out_h, out_w),
        dtype=input.dtype,
        device=input.device,
    )

    if output.numel() == 0:
        return output

    total_out = N * out_d * out_h * out_w

    def get_grid(meta):
        return (total_out,)

    grid_sampler_3d_kernel[get_grid](
        input,
        grid,
        output,
        N,
        C,
        in_d,
        in_h,
        in_w,
        out_d,
        out_h,
        out_w,
        *input.stride(),
        *grid.stride(),
        *output.stride(),
        total_out,
        interpolation_mode,
        padding_mode,
        align_corners,
    )

    return output
