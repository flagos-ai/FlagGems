def _infer_size(input_numel, size):
    """Infer the -1 dimension in the target size."""
    if isinstance(size, torch.Size):
        size = list(size)
    elif isinstance(size, (list, tuple)):
        size = list(size)
    else:
        raise TypeError("size must be a list/tuple/torch.Size of ints")

