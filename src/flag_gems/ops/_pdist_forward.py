    # Each program computes one output element (one pair of rows)
    # For output index pid, find the corresponding (i, j) pair where i < j
    #
    # pdist output ordering for N=4:
    # - Row 0 pairs: indices 0,1,2 -> pairs (0,1), (0,2), (0,3)
    # - Row 1 pairs: indices 3,4 -> pairs (1,2), (1,3)
    # - Row 2 pairs: index 5 -> pair (2,3)
    #
    # cum_before_i = i*N - i*(i+1)//2 = pairs before row i
    # For N=4: cum_before_0=0, cum_before_1=3, cum_before_2=5, cum_before_3=6
    #
    # Find i such that: cum_before_i <= pid < cum_before_{i+1}
    # Then row_i = i, and col_offset = pid - cum_before_i, row_j = i + 1 + col_offset

    # Linear search for row
    # We want to find the largest i where cum_before_i <= pid
