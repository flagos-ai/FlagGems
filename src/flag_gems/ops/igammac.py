def _launch_igammac(out: torch.Tensor, a: torch.Tensor, x: torch.Tensor):
    assert (
        a.device.type == flag_gems.device
        and x.device.type == flag_gems.device
        and out.device.type == flag_gems.device
    ), f"All tensors must be {flag_gems.device} tensors"
    assert (
        out.numel() == a.numel() == x.numel()
    ), "All tensors must have the same number of elements"
    assert out.device == a.device == x.device, "All tensors must be on the same device"

    a_in = a
    x_in = x
    out_in = out

    if not a_in.is_floating_point():
        a_in = a_in.to(torch.get_default_dtype())
    if not x_in.is_floating_point():
        x_in = x_in.to(torch.get_default_dtype())

    if a_in.dtype != out_in.dtype:
        a_in = a_in.to(out_in.dtype)
    if x_in.dtype != out_in.dtype:
        x_in = x_in.to(out_in.dtype)

    a_contig = a_in.contiguous()
    x_contig = x_in.contiguous()
    out_was_noncontig = not out_in.is_contiguous()
    out_contig = out_in.contiguous() if out_was_noncontig else out_in

    n_elements = out_contig.numel()
    grid = lambda meta: (triton.cdiv(n_elements, meta["BLOCK_SIZE"]),)

    # Check whether asymptotic expansion is needed for any element
    need_asym = False
    if n_elements > 0:
        a_max = a_contig.max().item()
        if a_max > 20:
            diff_ratio = (x_contig - a_contig).abs() / a_contig
            mask_asym = (a_contig > 20) & (diff_ratio < 0.3)
            mask_asym = mask_asym | (
                (a_contig > 200) & (diff_ratio < 4.5 / a_contig.sqrt())
            )
            need_asym = mask_asym.any().item()

    if need_asym:
        n_asym = mask_asym.sum().item()
        n_rest = n_elements - n_asym
        d_ptr = _get_d()

        if n_rest == 0:
            igammac_asym_kernel[grid](
                a_contig,
                x_contig,
                out_contig,
                n_elements,
                d_ptr,
                GAMMAINC_LOWER=False,
            )
        elif n_asym == 0:
            igammac_kernel[grid](
                a_contig,
                x_contig,
                out_contig,
                n_elements,
                GAMMAINC_LOWER=False,
                SERIES_ITERS=50,
                CF_ITERS=50,
            )
        else:
            a_asym = a_contig[mask_asym]
            x_asym = x_contig[mask_asym]
            out_asym = torch.empty(
                int(n_asym), dtype=out_contig.dtype, device=flag_gems.device
            )
            grid_a = lambda meta: (triton.cdiv(int(n_asym), meta["BLOCK_SIZE"]),)
            igammac_asym_kernel[grid_a](
                a_asym,
                x_asym,
                out_asym,
                int(n_asym),
                d_ptr,
                GAMMAINC_LOWER=False,
            )

            a_rest = a_contig[~mask_asym]
            x_rest = x_contig[~mask_asym]
            out_rest = torch.empty(
                int(n_rest), dtype=out_contig.dtype, device=flag_gems.device
            )
            grid_r = lambda meta: (triton.cdiv(int(n_rest), meta["BLOCK_SIZE"]),)
            igammac_kernel[grid_r](
                a_rest,
                x_rest,
                out_rest,
                int(n_rest),
                GAMMAINC_LOWER=False,
                SERIES_ITERS=50,
                CF_ITERS=50,
            )

            out_contig[mask_asym] = out_asym
            out_contig[~mask_asym] = out_rest
    else:
        igammac_kernel[grid](
            a_contig,
            x_contig,
            out_contig,
            n_elements,
            GAMMAINC_LOWER=False,
            SERIES_ITERS=50,
            CF_ITERS=50,
        )

    if out_was_noncontig:
        out_in.copy_(out_contig)
    return out_contig


def igammac(a: torch.Tensor, x: torch.Tensor, *, out: torch.Tensor = None):
    logger.debug("GEMS IGAMMAC")
    if a.device.type != flag_gems.device:
        raise ValueError(
            f"igammac: first input tensor must be on {flag_gems.device} device"
        )
    if x.device.type != flag_gems.device:
        raise ValueError(
            f"igammac: second input tensor must be on {flag_gems.device} device"
        )

    if out is None:
        if not a.is_floating_point():
            a = a.to(torch.get_default_dtype())
        if not x.is_floating_point():
            x = x.to(torch.get_default_dtype())
        out_dtype = torch.promote_types(a.dtype, x.dtype)
        out = torch.empty_like(a, dtype=out_dtype, device=a.device)
    else:
        if out.device.type != flag_gems.device:
            raise ValueError(
                f"igammac_out: output tensor must be on {flag_gems.device} device"
            )
        if not out.is_floating_point():
            raise TypeError("igammac_out: output tensor must be a floating point type")
        if a.numel() != x.numel() or a.numel() != out.numel():
            raise ValueError(
                "igammac_out: input and output must have the same number of elements"
            )
    _launch_igammac(out, a, x)
    return out


def igammac_out(a: torch.Tensor, x: torch.Tensor, out: torch.Tensor):
    logger.debug("GEMS IGAMMAC_OUT")
    return igammac(a, x, out=out)
